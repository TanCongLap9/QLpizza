CREATE PROCEDURE PROC_NGUOIDUNG_DANGNHAP(@TenDangNhap AS varchar(50)) AS BEGIN
    SELECT * FROM NGUOIDUNG
        LEFT JOIN QUYENHAN ON NGUOIDUNG.MaQuyen = QUYENHAN.MaQuyen
        LEFT JOIN CHITIETQUYENHAN ON QUYENHAN.MaQuyen = CHITIETQUYENHAN.MaQuyen
    WHERE TenDangNhap = @TenDangNhap
END
GO
CREATE PROCEDURE PROC_HOADON_TONGTIEN(@MaHoaDon AS char(9), @MaPizza AS char(5) = NULL) AS BEGIN
	SELECT COALESCE(
		(SELECT SUM(GiaBan * SoLuong) FROM CHITIETHOADON
		  JOIN PIZZA ON CHITIETHOADON.MaPizza = PIZZA.MaPizza
		WHERE MaHoaDon = @MaHoaDon AND (@MaPizza IS NULL OR PIZZA.MaPizza = @MaPizza)),
		0
	);
END
GO
CREATE FUNCTION FUNC_HOADON_TONGTIEN(@MaHoaDon AS char(9), @MaPizza AS char(5) = NULL) RETURNS BIGINT AS BEGIN
	RETURN(SELECT COALESCE(
		(SELECT SUM(GiaBan * SoLuong) FROM CHITIETHOADON
		  JOIN PIZZA ON CHITIETHOADON.MaPizza = PIZZA.MaPizza
		WHERE MaHoaDon = @MaHoaDon AND (@MaPizza IS NULL OR PIZZA.MaPizza = @MaPizza)),
		0
	));
END
GO
CREATE PROCEDURE PROC_NHAP_TONGTIEN(@MaNhap AS char(5), @MaNL AS char(5) = NULL) AS BEGIN
	SELECT COALESCE(
		(SELECT SUM(DonGia * CHITIETNHAP.SoLuong) FROM CHITIETNHAP
            JOIN NGUYENLIEU ON CHITIETNHAP.MaNL = NGUYENLIEU.MaNL
        WHERE MaNhap = @MaNhap AND (@MaNL IS NULL OR NGUYENLIEU.MaNL = @MaNL)),
		0
	);
END
GO
CREATE FUNCTION FUNC_PIZZA_GIAVON(@MaPizza AS char(5), @MaCongThuc AS char(5) = NULL) RETURNS TABLE AS RETURN
    SELECT SUM(DonGia * CHITIETCONGTHUC.SoLuong) - KhauHao AS GiaVon FROM PIZZA
        JOIN CONGTHUC ON PIZZA.MaPizza = CONGTHUC.MaPizza
        JOIN CHITIETCONGTHUC ON CONGTHUC.MaCongThuc = CHITIETCONGTHUC.MaCongThuc
        JOIN NGUYENLIEU ON CHITIETCONGTHUC.MaNL = NGUYENLIEU.MaNL
    WHERE PIZZA.MaPizza = @MaPizza AND (@MaCongThuc IS NULL OR CONGTHUC.MaCongThuc = @MaCongThuc)
	GROUP BY KhauHao;
GO
CREATE PROCEDURE PROC_PIZZA_GIAVONBINHQUAN(@MaPizza AS char(5)) AS BEGIN
    SELECT COALESCE((SELECT AVG(GiaVon) FROM FUNC_PIZZA_GIAVON(@MaPizza, DEFAULT)), 0);
END
GO
CREATE FUNCTION FUNC_PIZZA_GIAVONBINHQUAN(@MaPizza AS char(5)) RETURNS BIGINT AS BEGIN
    RETURN (SELECT COALESCE((SELECT AVG(GiaVon) FROM FUNC_PIZZA_GIAVON(@MaPizza, DEFAULT)), 0));
END
GO
CREATE PROCEDURE PROC_GIOHANG_PIZZA(@MaND AS char(5)) AS BEGIN
	SELECT * FROM GIOHANG
	  JOIN PIZZA ON GIOHANG.MaPizza = PIZZA.MaPizza
	WHERE MaND = @MaND;
END
GO
CREATE PROCEDURE PROC_GIOHANG_THANHTIEN(@MaND AS char(5)) AS BEGIN
	SELECT COALESCE(
    	(SELECT SUM(SoLuong * GiaBan) FROM GIOHANG
    	  JOIN PIZZA ON GIOHANG.MaPizza = PIZZA.MaPizza
    	WHERE MaND = @MaND),
    	0
	);
END
GO
CREATE PROCEDURE PROC_CONGTHUC_CHIPHI(@MaCongThuc AS char(5)) AS
    SELECT SUM(DonGia * CHITIETCONGTHUC.SoLuong) - KhauHao AS GiaVon FROM CONGTHUC
        JOIN CHITIETCONGTHUC ON CONGTHUC.MaCongThuc = CHITIETCONGTHUC.MaCongThuc
        JOIN NGUYENLIEU ON CHITIETCONGTHUC.MaNL = NGUYENLIEU.MaNL
    WHERE CONGTHUC.MaCongThuc = @MaCongThuc
	GROUP BY KhauHao;
GO
CREATE FUNCTION FUNC_HOADON_TRADU(@MaHoaDon AS char(9)) RETURNS bit AS BEGIN
  RETURN CASE WHEN (
    SELECT 1
    FROM HOADON
    WHERE MaHoaDon = @MaHoaDon AND
          KhachTra >= dbo.FUNC_HOADON_TONGTIEN(@MaHoaDon, DEFAULT)
  ) = 1 THEN 1 ELSE 0 END
END
GO
CREATE PROCEDURE PROC_THONGKE_PIZZA(@MinDate AS date, @MaxDate AS date) AS BEGIN
  SELECT PIZZA.MaPizza, TenPizza, TenLoai, dbo.FUNC_PIZZA_GIAVONBINHQUAN(PIZZA.MaPizza) AS GiaVonBinhQuan, GiaBan, COALESCE(SUM(SoLuong), 0) AS BanDuoc, COALESCE(SUM(GiaBan * SoLuong), 0) AS DoanhThu, COALESCE(SUM(GiaBan * SoLuong), 0) - COALESCE(SUM(dbo.FUNC_PIZZA_GIAVONBINHQUAN(PIZZA.MaPizza) * SoLuong), 0) AS LoiNhuan
  FROM PIZZA
    LEFT JOIN LOAIPIZZA ON PIZZA.MaLoai = LOAIPIZZA.MaLoai
    LEFT JOIN CHITIETHOADON ON PIZZA.MaPizza = CHITIETHOADON.MaPizza
    LEFT JOIN HOADON ON CHITIETHOADON.MaHoaDon = HOADON.MaHoaDon
  WHERE NgayDat BETWEEN @MinDate AND @MaxDate AND dbo.FUNC_HOADON_TRADU(HOADON.MaHoaDon) = 1
  GROUP BY PIZZA.MaPizza, TenPizza, TenLoai, GiaBan
  ORDER BY TenLoai, TenPizza
END
GO
CREATE PROCEDURE PROC_THONGKE_NGUYENLIEU(@MaxDate AS date) AS BEGIN
  SELECT MaNL, TenNL, SoLuong, DonViTinh, DonGia, SoLuong * DonGia AS TongTien
  FROM NGUYENLIEU
END
GO
CREATE PROCEDURE PROC_THONGKE_HOADON(@MinDate AS date, @MaxDate AS date) AS BEGIN
  SELECT HOADON.MaHoaDon, NHANVIEN.HoTen AS NhanVien, KHACHHANG.HoTen AS KhachHang, NgayDat, dbo.FUNC_HOADON_TONGTIEN(HOADON.MaHoaDon, DEFAULT) AS DoanhThu, COALESCE(SUM(GiaBan * SoLuong), 0) - COALESCE(SUM(dbo.FUNC_PIZZA_GIAVONBINHQUAN(PIZZA.MaPizza) * SoLuong), 0) AS LoiNhuan
  FROM HOADON
    JOIN CHITIETHOADON ON HOADON.MaHoaDon = CHITIETHOADON.MaHoaDon
    JOIN PIZZA ON CHITIETHOADON.MaPizza = PIZZA.MaPizza
    JOIN NGUOIDUNG AS KHACHHANG ON HOADON.MaKH = KHACHHANG.MaND
    JOIN NGUOIDUNG AS NHANVIEN ON HOADON.MaKH = NHANVIEN.MaND
  WHERE NgayDat BETWEEN @MinDate AND @MaxDate AND dbo.FUNC_HOADON_TRADU(HOADON.MaHoaDon) = 1
  GROUP BY HOADON.MaHoaDon, NHANVIEN.HoTen, KHACHHANG.HoTen, NgayDat
  ORDER BY NgayDat
END

