  TrangThaiDangNhap bit NOT NULL,
  MaCongViec char(5) NOT NULL,
  MaQuyen char(5) NOT NULL,
  PRIMARY KEY (MaND),
  FOREIGN KEY (MaCongViec) REFERENCES CONGVIEC(MaCongViec),
  FOREIGN KEY (MaQuyen) REFERENCES QUYENHAN(MaQuyen),
  CHECK (MaCongViec = 'KH00V' OR YEAR(GetDate()) - YEAR(NgaySinh) >= 18)
);
GO
CREATE TABLE PIZZA (
  MaPizza char(5) NOT NULL,
  TenPizza nvarchar(50) NOT NULL,
  FileHinh varchar(100) NOT NULL,
  GiaBan int NOT NULL,
  MaLoai char(5) NOT NULL,
  NgungBan bit NOT NULL,
  PRIMARY KEY (MaPizza),
  FOREIGN KEY (MaLoai) REFERENCES LOAIPIZZA(MaLoai),
  CHECK (GiaBan > 0)
);
GO
CREATE TABLE CONGTHUC (
  MaCongThuc char(5) NOT NULL,
  TacGia nvarchar(50) NOT NULL,
  MaPizza char(5) NOT NULL,
  KhauHao int NOT NULL,
  PRIMARY KEY (MaCongThuc),
  FOREIGN KEY (MaPizza) REFERENCES PIZZA(MaPizza),
  CHECK (KhauHao >= 0)
);
GO
CREATE TABLE CHITIETCONGTHUC (
  MaCongThuc char(5) NOT NULL,
  MaNL char(5) NOT NULL,
  SoLuong float NOT NULL,
  ThoiGianCheBien float NOT NULL,
  PRIMARY KEY (MaCongThuc, MaNL),
  FOREIGN KEY (MaCongThuc) REFERENCES CONGTHUC(MaCongThuc),
  FOREIGN KEY (MaNL) REFERENCES NGUYENLIEU(MaNL),
  CHECK (ThoiGianCheBien > 0),
  CHECK (SoLuong > 0)
);
GO
CREATE TABLE HOADON (
  MaHoaDon char(9) NOT NULL,
  MaKH char(5) NOT NULL,
  MaNV char(5) NULL,
  MaBan char(4) NOT NULL,
  NgayDat date NOT NULL,
  ThoiGianDat time(0) NOT NULL,
  PhuongThucThanhToan int NOT NULL,
  KhachTra int NOT NULL,
  GhiChu nvarchar(1023) NOT NULL,
  PRIMARY KEY (MaHoaDon),
  FOREIGN KEY (MaKH) REFERENCES NGUOIDUNG(MaND),
  FOREIGN KEY (MaNV) REFERENCES NGUOIDUNG(MaND),
  FOREIGN KEY (MaBan) REFERENCES BAN(MaBan),
  CHECK (KhachTra >= 0)
);
GO
CREATE TABLE CHITIETHOADON (
  MaHoaDon char(9) NOT NULL,
  MaPizza char(5) NOT NULL,
  SoLuong int NOT NULL CHECK (SoLuong > 0),
  PRIMARY KEY (MaHoaDon, MaPizza),
  FOREIGN KEY (MaHoaDon) REFERENCES HOADON(MaHoaDon) ON DELETE CASCADE,
  FOREIGN KEY (MaPizza) REFERENCES PIZZA(MaPizza)
);
GO
CREATE TABLE NHAP (
  MaNhap char(5) NOT NULL,
  NgayNhap date NOT NULL,
  ThoiGianNhap time(0) NOT NULL,
  MaNV char(5) NOT NULL,
  MaNCC char(5) NOT NULL,
  PRIMARY KEY (MaNhap),
  FOREIGN KEY (MaNV) REFERENCES NGUOIDUNG(MaND),
  FOREIGN KEY (MaNCC) REFERENCES NHACUNGCAP(MaNCC)
);
GO
CREATE TABLE CHITIETNHAP (
  MaNhap char(5) NOT NULL,
  MaNL char(5) NOT NULL,
  SoLuong float NOT NULL CHECK (SoLuong > 0),
  PRIMARY KEY (MaNhap, MaNL),
  FOREIGN KEY (MaNhap) REFERENCES NHAP(MaNhap) ON DELETE CASCADE,
  FOREIGN KEY (MaNL) REFERENCES NGUYENLIEU(MaNL)
);
GO
CREATE TABLE GIOHANG (
  MaND char(5) NOT NULL,
  MaPizza char(5) NOT NULL,
  SoLuong int NOT NULL CHECK (SoLuong > 0),
  PRIMARY KEY (MaND, MaPizza),
  FOREIGN KEY (MaND) REFERENCES NGUOIDUNG(MaND),
  FOREIGN KEY (MaPizza) REFERENCES PIZZA(MaPizza)
);
GO
CREATE TABLE CHEBIEN (
  MaCheBien char(9) NOT NULL,
  MaCongThuc char(5) NOT NULL,
  MaHoaDon char(9) NOT NULL,
  MaNV char(5) NOT NULL,
  NgayLap date NOT NULL,
  ThoiGianLap time(0) NOT NULL,
  PRIMARY KEY (MaCheBien),
  FOREIGN KEY (MaCongThuc) REFERENCES CONGTHUC(MaCongThuc),
  FOREIGN KEY (MaNV) REFERENCES NGUOIDUNG(MaND),
  FOREIGN KEY (MaHoaDon) REFERENCES HOADON(MaHoaDon)
