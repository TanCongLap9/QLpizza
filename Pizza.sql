CREATE DATABASE PIZZA;
GO
USE PIZZA;
GO
CREATE TABLE QUYENHAN (
  MaQuyen char(5) NOT NULL,
  TenQuyen nvarchar(50) NOT NULL,
  PRIMARY KEY (MaQuyen)
);
GO
CREATE TABLE CHITIETQUYENHAN (
  MaQuyen char(5) NOT NULL,
  TenForm varchar(20) NOT NULL,
  DuocDoc int NOT NULL, /* 0: Không cho đọc, 1: Cho đọc toàn bộ dữ liệu, 2: Cho đọc dữ liệu của tôi */
  DuocGhi int NOT NULL,
  PRIMARY KEY (MaQuyen, TenForm),
  FOREIGN KEY (MaQuyen) REFERENCES QUYENHAN(MaQuyen)
);
GO
CREATE TABLE CONGVIEC (
  MaCongViec char(5) NOT NULL,
  TenCongViec nvarchar(40) NOT NULL,
  MoTa nvarchar(100) NOT NULL,
  MucLuong int NOT NULL CHECK (MucLuong >= 0),
  PRIMARY KEY (MaCongViec)
);
GO
CREATE TABLE LOAIPIZZA (
  MaLoai char(5) NOT NULL,
  TenLoai nvarchar(40) NOT NULL,
  PRIMARY KEY (MaLoai)
);
GO
CREATE TABLE NHACUNGCAP (
  MaNCC char(5) NOT NULL,
  TenNCC nvarchar(50) NOT NULL,
  DiaChi nvarchar(100) NOT NULL,
  SDT varchar(12) NOT NULL CHECK (
    SDT LIKE '0[0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9]' OR
    SDT LIKE '0[0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9]' OR
    SDT LIKE '0[0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9]'
  ),
  PRIMARY KEY (MaNCC)
);
GO
CREATE TABLE NGUYENLIEU (
  MaNL char(5) NOT NULL,
  TenNL nvarchar(50) NOT NULL,
  SoLuong float NOT NULL CHECK (SoLuong >= 0),
  DonViTinh nvarchar(10) NOT NULL,
  DonGia int NOT NULL CHECK (DonGia > 0),
  PRIMARY KEY (MaNL)
);
GO
CREATE TABLE BAN (
  MaBan char(4) NOT NULL,
  TenBan nvarchar(50) NOT NULL,
  PRIMARY KEY (MaBan)
);
GO
CREATE TABLE NGUOIDUNG (
  MaND char(5) NOT NULL,
  HoTen nvarchar(40) NOT NULL,
  GioiTinh nvarchar(3) NOT NULL CHECK (GioiTinh IN (N'Nam', N'Nữ')),
  TenDangNhap varchar(50) NOT NULL UNIQUE,
  MatKhau char(64) NOT NULL,
  Email varchar(50) NOT NULL CHECK (Email LIKE '%@%.%'),
  NgaySinh date NOT NULL,
  SDT varchar(12) NOT NULL CHECK (
    SDT LIKE '0[0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9]' OR
    SDT LIKE '0[0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9]' OR
    SDT LIKE '0[0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9][0-9]'
