CREATE TRIGGER TRG_CHITIETNHAP_INSERT ON CHITIETNHAP FOR INSERT AS BEGIN
  UPDATE NGUYENLIEU SET SoLuong = SoLuong + (SELECT SoLuong FROM inserted) WHERE MaNL = (SELECT MaNL FROM inserted)
END
