CREATE TRIGGER TRG_CHITIETNHAP_DELETE ON CHITIETNHAP FOR DELETE AS BEGIN
  DECLARE @MaNL AS char(5), @SoLuong AS float
  DECLARE CUR_NGUYENLIEU CURSOR FOR
    SELECT MaNL, SoLuong FROM deleted
  OPEN CUR_NGUYENLIEU
  FETCH NEXT FROM CUR_NGUYENLIEU INTO @MaNL, @SoLuong
  WHILE @@FETCH_STATUS = 0 BEGIN
    IF (SELECT SoLuong FROM NGUYENLIEU WHERE MaNL = @MaNL) - @SoLuong < 0 BEGIN
	    PRINT N'Không thể xoá chi tiết nhập do số lượng nguyên liệu không đủ.'
		ROLLBACK TRANSACTION
		BREAK
	END
	UPDATE NGUYENLIEU SET SoLuong = SoLuong - @SoLuong WHERE MaNL = @MaNL
    FETCH NEXT FROM CUR_NGUYENLIEU INTO @MaNL, @SoLuong
  END
  CLOSE CUR_NGUYENLIEU
  DEALLOCATE CUR_NGUYENLIEU
END
GO
CREATE TRIGGER TRG_CHEBIEN_INSERT ON CHEBIEN FOR INSERT AS BEGIN
  DECLARE @MaNL AS char(5), @SoLuong AS float
  DECLARE CUR_NGUYENLIEU CURSOR FOR
    SELECT MaNL, SoLuong FROM inserted
	  JOIN CHITIETCONGTHUC ON inserted.MaCongThuc = CHITIETCONGTHUC.MaCongThuc
  OPEN CUR_NGUYENLIEU
  FETCH NEXT FROM CUR_NGUYENLIEU INTO @MaNL, @SoLuong
  WHILE @@FETCH_STATUS = 0 BEGIN
    IF (SELECT SoLuong FROM NGUYENLIEU WHERE MaNL = @MaNL) - @SoLuong < 0 BEGIN
	    PRINT N'Không đủ nguyên liệu để làm bánh pizza. Vui lòng chọn công thức khác hoặc gọi bộ phận kho làm phiếu nhập.'
		ROLLBACK TRANSACTION
		BREAK
	END
    UPDATE NGUYENLIEU SET SoLuong = SoLuong - @SoLuong WHERE MaNL = @MaNL
    FETCH NEXT FROM CUR_NGUYENLIEU INTO @MaNL, @SoLuong
  END
  CLOSE CUR_NGUYENLIEU
  DEALLOCATE CUR_NGUYENLIEU
END
GO
CREATE TRIGGER TRG_CHEBIEN_DELETE ON CHEBIEN FOR DELETE AS BEGIN
  DECLARE @MaNL AS char(5), @SoLuong AS float
  DECLARE CUR_NGUYENLIEU CURSOR FOR
    SELECT MaNL, SoLuong FROM deleted
	  JOIN CHITIETCONGTHUC ON deleted.MaCongThuc = CHITIETCONGTHUC.MaCongThuc
  OPEN CUR_NGUYENLIEU
  FETCH NEXT FROM CUR_NGUYENLIEU INTO @MaNL, @SoLuong
